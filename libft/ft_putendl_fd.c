#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	x;

	if (!s)
		return ;
	x = 0;
	while (s[x] != '\0')
	{
		write(fd, &s[x], 1);
		x++;
	}
	write(fd, "\n", 1);
}
