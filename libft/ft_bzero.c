#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	size_t	x;
	char	*cstr;

	cstr = ((char *)str);
	x = 0;
	if (n > 0)
	{
		while (x < n)
		{
			cstr[x] = '\0';
			x++;
		}
	}
}
